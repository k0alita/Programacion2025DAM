package Trimestre1.Objetos.Objetos2.Ejercicio2;

public class DiscoDuro {
    private String marca;
    private String tipo;
    private int capaciodad;

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public int getCapaciodad() {
        return capaciodad;
    }

    public void setCapaciodad(int capaciodad) {
        this.capaciodad = capaciodad;
    }
}

