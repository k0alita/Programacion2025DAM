package Trimestre1.Objetos.Objetos2.Ejercicio2;

public class MicroProcesador {
    private String marca;
    private String modelo;
    private int num_nucl;
    private float velocidad_base;
    private String socket;

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public int getNum_nucl() {
        return num_nucl;
    }

    public void setNum_nucl(int num_nucl) {
        this.num_nucl = num_nucl;
    }

    public float getVelocidad_base() {
        return velocidad_base;
    }

    public void setVelocidad_base(float velocidad_base) {
        this.velocidad_base = velocidad_base;
    }

    public String getSocket() {
        return socket;
    }

    public void setSocket(String socket) {
        this.socket = socket;
    }
}

