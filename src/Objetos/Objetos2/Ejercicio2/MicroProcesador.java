package Objetos.Objetos2.Ejercicio2;

public class MicroProcesador {
    private String marca;
    private String modelo;
    private int num_nuc;
    private float velocidad_base;
    private String socket;

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public int getNum_nuc() {
        return num_nuc;
    }

    public void setNum_nuc(int num_nuc) {
        this.num_nuc = num_nuc;
    }

    public float getVelocidad_base() {
        return velocidad_base;
    }

    public void setVelocidad_base(float velocidad_base) {
        this.velocidad_base = velocidad_base;
    }

    public String getSocket() {
        return socket;
    }

    public void setSocket(String socket) {
        this.socket = socket;
    }
}

